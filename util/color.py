def get_layer_color(doc) -> dict:
    layers_color = {}
    layers = doc.layers
    key = list(layers.entries.keys())
    for i, layer in enumerate(layers):
        lay_co = layer.color
        layers_color[key[i]] = color_map[str(lay_co)]
    return layers_color
